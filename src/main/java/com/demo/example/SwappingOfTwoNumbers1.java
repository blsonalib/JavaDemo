package com.demo.example;

public class SwappingOfTwoNumbers1 {
    public static void main(String[] args) {
        int a = 4;
        int b = 50;
        int temp;
        temp = a;      //4
        a = b ;         //5
        b = temp;       //4
        System.out.println("Value of a : "+a);
        System.out.println("Value Of b : "+b);

    }
}
